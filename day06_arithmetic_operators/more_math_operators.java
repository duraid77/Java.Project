package day06_arithmetic_operators;

public class more_math_operators {
    public static void main(String[] args){
        int toyota=431;
        int hondas = 233;
        int vw = 2;
        int tesla = 20;
        int nissan = 1;
        int bmw = 155;
        int totalCarsInParking = toyota + hondas + vw + tesla + nissan + bmw;

        System.out.println("Total cars in Parking Garage is: " + totalCarsInParking);


        int slices = 8;
        int people = 4;
        int slicesPerPerson = slices / people;

        System.out.println("Number of slices per person is " + slicesPerPerson);

    }
}
