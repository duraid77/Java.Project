package day5_primitives_concatanation;

public class primitivesExample {
    public static void main(String[] args){
        byte bytevalue=44;
        short shortValue= 20000;
        int intValue=2000000000;
        long longValue=5639536522515L;
        //decimals value
        float floatValue=1582.3651F;
        double doubleValue=2366525.25652D;
        //character type
        char charValue='c';
        //boolean
        boolean booleanValue = true;
        boolean booleanValueF = false;
        System.out.println(bytevalue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(booleanValue);
        System.out.println(booleanValueF);
        System.out.println(charValue);






    }
}
